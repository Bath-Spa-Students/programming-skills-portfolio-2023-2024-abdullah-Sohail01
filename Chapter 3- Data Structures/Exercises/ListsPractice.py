#append:

list_1 = [2,4,6,8,10]
list_1.append(12)
print(list_1)

#Index method:

list_2 = [5,10,15,20,25,30]
print(list_2.index(25))

#Sort:

list_3 = [4,8,16,12,20]
list_3.sort()
print(list_3)

#Reverse:

list_4 = [10,20,30,40,50]
list_4.reverse()
print(list_4)

#remove:

list_5 = [50,100,150,200]
list_5.remove(150)
print(list_5)

#Min and Max:

print(max(list_5))
print(min(list_5))






